public class NauticalMiles {
    public static void main(String[] args){
        double kmInNauticalMile = 1.852;
        double milesInNauticalMile = 1.150779;
        double numOfNauticalMiles = 10.0;
        System.out.println("Number of Nautical Miles: " + numOfNauticalMiles + "\n" + "Number of KM: " + (numOfNauticalMiles*kmInNauticalMile) + "\n" + "Number of Miles: " + (numOfNauticalMiles*milesInNauticalMile));
    }
}
